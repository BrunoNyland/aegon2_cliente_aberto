	def RefreshCategoria(self, categoria):
		if self.textoCategoria[categoria].IsShow():
			self.SetCategoria(categoria)

	def SetCategoria(self, categoria):
		for cat in self.textoCategoria:
			cat.Hide()
		self.textoCategoria[categoria].Show()

		self.btnListaEmojis = []
		self.btnNewEmojis = []
		self.EmojisRecentesList = []

		self.EmojisCountMax = 80
		self.EmojiStep = 30
		self.BasePos = 0
		self.ScollPos = 0
		self.ScrollBar.SetPos(0)

		if (categoria == 0):
			f = open(EMOJI_FILE_NAME, "r", "folder")
			lines = f.readlines()
			f.close()

			for line in lines:
				tokens = line[:-1].split("\t")
				if len(tokens) == 2:
					btnListaEmojis = ui.Button()
					btnListaEmojis.SetParent(self)
					btnListaEmojis.SetUpVisual("d:/ymir work/ui/emoji_button/%s.png" % tokens[1])
					btnListaEmojis.SetOverVisual("d:/ymir work/ui/emoji_button/%s.png" % tokens[1])
					btnListaEmojis.SetDownVisual("d:/ymir work/ui/emoji_button/%s.png" % tokens[1])
					btnListaEmojis.SetEvent(self.OnPressEmojis, "|c00000000|E"+tokens[1]+"|e    |e|r", 0, 0, 0)

					toolTip = ui.Ballon()
					toolTip.SetParent(btnListaEmojis)
					toolTip.SetPosition(0, -38)
					toolTip.SetWindowHorizontalAlignCenter()
					toolTip.Hide()
					if int(tokens[0]) > 1:
						toolTip.SetText("Emoji usado %s vezes." % str(tokens[0]))
					else:
						toolTip.SetText("Emoji usado uma vez.")
					self.toolTip = toolTip

					btnListaEmojis.SetToolTipWindow(self.toolTip)
					self.btnListaEmojis.append(btnListaEmojis)
		else:
			for i in range(len(EMOJIS_CODIGOS_TEXTOS_LISTA[categoria])):
				codetext = EMOJIS_CODIGOS_TEXTOS_LISTA[categoria][i][1]
				main = codetext
				main = main.replace("|E", "")
				main = main.replace("|e", "")
				btnListaEmojis = ui.Button()
				btnListaEmojis.SetParent(self)
				btnListaEmojis.SetUpVisual("d:/ymir work/ui/emoji_button/%s.png" % main)
				btnListaEmojis.SetOverVisual("d:/ymir work/ui/emoji_button/%s.png" % main)
				btnListaEmojis.SetDownVisual("d:/ymir work/ui/emoji_button/%s.png" % main)
				newBtn = 0
				if EMOJIS_CODIGOS_TEXTOS_LISTA[categoria][i][1] == codetext and EMOJIS_CODIGOS_TEXTOS_LISTA[categoria][i][2] != "":
					newBtn = 1
				btnListaEmojis.SetEvent(self.OnPressEmojis, "|c00000000|E"+main+"|e    |e|r", newBtn, categoria, i)
				self.btnListaEmojis.append(btnListaEmojis)

