class EmojisBoard(ui.Window):
	def __init__(self):
		ui.Window.__init__(self)
		self.__LoadScript()
		self.chatLine = None

	def __del__(self):
		ui.Window.__del__(self)

	def __LoadScript(self):
		self.SetPosition(0, 0)
		self.SetSize(600, 238)

		## Imagem de fundo do chat emojis
		board = ui.ExpandedImageBox()
		board.SetParent(self)
		board.AddFlag("not_pick")
		board.LoadImage("d:/ymir work/ui/pattern/chat_fundo.png")
		board.SetPosition(0, 6)
		self.board = board

		## Botões do menu emojis
		self.btnEmojis = []
		pos = 50
		for i in range(len(EMOJIS_CATEGORIAS_LISTA)):
			btn = ui.RadioButton()
			btn.SetParent(self)
			btn.SetUpVisual("d:/ymir work/ui/emoji/%s_norm.png" % i)
			btn.SetOverVisual("d:/ymir work/ui/emoji/%s_over.png" % i)
			btn.SetDownVisual("d:/ymir work/ui/emoji/%s_down.png" % i)
			btn.SetPosition(pos + (6 * (i + 1)), 15)
			btn.SetEvent(self.SetCategoriaEmojis, i)
			btn.Show()

			toolTip = ui.Ballon()
			toolTip.SetParent(btn)
			toolTip.SetPosition(0, -38)
			toolTip.SetWindowHorizontalAlignCenter()
			toolTip.Hide()
			toolTip.SetText(EMOJIS_CATEGORIAS_LISTA[i])
			self.toolTip = toolTip

			btn.SetToolTipWindow(self.toolTip)
			self.btnEmojis.append(btn)
			pos += 40

		## Lista dos Emojis
		listBoard = EmojisListBoard()
		listBoard.SetParent(self)
		self.listBoard = listBoard

	def Destroy(self):
		self.listBoard = None
		del self.btnEmojis[:]

	def Open(self, chatLine):
		self.chatLine = chatLine
		if self.IsShow():
			self.chatLine.DelEmojisListBoard()
			self.listBoard.Close()
			self.board.Hide()
			self.Hide()
		else:
			self.listBoard.Open(self.chatLine)
			self.SetCategoriaEmojis(0)
			self.chatLine.SetEmojisListBoard(self.listBoard)
			self.board.Show()
			self.Show()

		return True

	def Close(self):
		self.listBoard.Close()
		self.board.Hide()
		self.Hide()
		return True

	def SetCategoriaEmojis(self, index):
		self.listBoard.SetCategoria(index)
		for btn in self.btnEmojis:
			btn.SetUp()
		self.btnEmojis[index].Down()

