	def OnScroll(self):
		self.ScollPos = int(self.ScrollBar.GetPos() * self.BasePos + 1)
		if self.ScollPos > self.BasePos:
			self.ScollPos = self.BasePos

		self.RefreshList()

	def RefreshList(self):
		for btn in self.btnListaEmojis:
			btn.Hide()

		pos = int((self.ScollPos - 1) * self.EmojisCountMax)
		x = 16
		y = 38
		count = 0
		for i in range(len(self.btnListaEmojis)):
			if i >= pos and count < self.EmojisCountMax:
				if count >= 0 and count <= 15:
					self.btnListaEmojis[i].SetPosition(x + (count * self.EmojiStep), y)
				elif count >= 16 and count <= 31:
					self.btnListaEmojis[i].SetPosition(x + ((count - 16) * self.EmojiStep), y + (1 * self.EmojiStep))
				elif count >= 32 and count <= 47:
					self.btnListaEmojis[i].SetPosition(x + ((count - 32) * self.EmojiStep), y + (2 * self.EmojiStep))
				elif count >= 48 and count <= 63:
					self.btnListaEmojis[i].SetPosition(x + ((count - 48) * self.EmojiStep), y + (3 * self.EmojiStep))
				elif count >= 64 and count <= 79:
					self.btnListaEmojis[i].SetPosition(x + ((count - 64) * self.EmojiStep), y + (4 * self.EmojiStep))
				else:
					return
				self.btnListaEmojis[i].Show()
				count += 1
			else:
				self.btnListaEmojis[i].Hide()

	def OnPressEmojis(self, texto, newBtn = 0, categoria = 0, codigo = 0):
		for btn in self.btnNewEmojis:
			btn.Hide()

		if newBtn > 0:
			pos = 170
			for x in range(6):
				main = EMOJIS_CODIGOS_TEXTOS_LISTA[categoria][codigo][x+1]
				main = main.replace("|E", "")
				main = main.replace("|e", "")
				tokens = main.split("/")

				btn = ui.Button()
				btn.SetParent(self)
				btn.SetUpVisual("d:/ymir work/ui/emoji_button/%s.png" % main)
				btn.SetOverVisual("d:/ymir work/ui/emoji_button/%s.png" % main)
				btn.SetDownVisual("d:/ymir work/ui/emoji_button/%s.png" % main)
				btn.SetPosition(pos + (10 * (x + 1)), 3)
				btn.SetEvent(self.OnPressEmojis, ("|c00000000|E"+str(tokens[0])+"/"+str(tokens[1])+"|e    |e|r"))
				btn.Show()
				self.btnNewEmojis.append(btn)
				pos += 20

			return

		ime.PasteString(texto)

	def OnRunMouseWheel(self, nLen):
		if nLen > 0:
			self.ScrollBar.OnUp()
		else:
			self.ScrollBar.OnDown()

